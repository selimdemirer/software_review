package j13_NestedLoop;

public class NestedLoopPractice {

    public static void main(String[] args) {

        for (int j = 0; j < 10; j++) {

            for (int i = 0; i < 10; i++) {
                System.out.print("* ");
            }

            System.out.println();
        }


    }

}

